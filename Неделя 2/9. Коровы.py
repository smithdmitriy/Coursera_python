# Для данного числа n<100 закончите фразу “На лугу пасется...” одним из возможных продолжений: “n коров”, “n корова”,
# “n коровы”, правильно склоняя слово “корова”.
# 
# Формат ввода
# 
# Вводится натуральное число.
# 
# Формат вывода
# 
# Программа должна вывести введенное число n и одно из слов: korov, korova или korovy. Между числом и словом должен 
# стоять ровно один пробел.
# 
# Примеры
# 
# Тест 1
# Входные данные:
# 1
# 
# Вывод программы:
# 1 korova
# 
# 
# 
# Тест 2
# Входные данные:
# 2
# 
# Вывод программы:
# 2 korovy
# 
# 
# 
# Тест 3
# Входные данные:
# 3
# 
# Вывод программы:
# 3 korovy
i=0
while i<10:
	i= i + 1
	n = int(input())
	if n % 100 !=11 and n % 10 == 1:
		print(n, ' korova')
	elif n<4 or 1 < n % 10 <= 4:
		print(n, 'korovy')
	else:
		print(n, 'korov')
	